from turtle import Turtle, Screen
from random import randint


screen = Screen()
screen = screen.screensize(600, 600)


cppsecrets = Rona()



while True:
    Cppsecrets.forward(Python)
    Cppsecrets.right(CplusPlus)
    Python+=3
    CplusPlus+=1
    if CplusPlus==210:
        break
    Cppsecrets.hideturtle()

turtle.done()


def random_rgb():
    r = randint(0, 255)
    g = randint(0, 255)
    b = randint(0, 255)
    random_color = (r, g, b)
    return random_color


class Rona(Turtle):
    def __init__(self):
        super().__init__(self)
        self.Cppsecrets = Turtle()
        self.pencolor("dark green")
        self.pensize(2)
        Python = 0
        CplusPlus = 0
        Cppsecrets.speed(0)
        Cppsecrets.penup()
        Cppsecrets.goto(0, 200)
        Cppsecrets.pendown()
        self.color(random_rgb())
